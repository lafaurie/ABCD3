package cu.uci.abcd.dao.test;

public class DaoTestException extends RuntimeException {

	public DaoTestException() {
		super();
	}

	public DaoTestException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DaoTestException(String message, Throwable cause) {
		super(message, cause);
	}

	public DaoTestException(String message) {
		super(message);
	}

	public DaoTestException(Throwable cause) {
		super(cause);
	}

	private static final long serialVersionUID = 1L;

}
